    public static List<Object[]> move_at_level(String name)
    {   Object []temp=pokemon_move_dict.get(name);

        // Create the Map to store the pairs
        List<Object[]> moveMap = new ArrayList<>();
        // Iterate over the array
        for (int i = 0; i < temp.length; i += 2) {
            Integer key = (Integer) temp[i];      // Even indices: Integer (key)
            Move value = (Move)temp[i + 1];  
            Object[] moveLevelPair = {key,value};// Odd indices: String (value)
            moveMap.add(moveLevelPair);            // Insert into map
        }
        //System.out.println(temp);
        // Print the map
        return moveMap;
    }
